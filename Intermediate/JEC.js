// Javascript Excecution Context

/* The JavaScript execution context is the environment in which JavaScript code is executed. 
It includes all the necessary information and settings for your code to run, such as variables, functions, scope, and more. 
There are two types of execution contexts: global and function. */

// 1) Global execution context

/* The global execution context is created when a JavaScript script first starts to run,
 and it represents the global scope in JavaScript. */


// 2) Function execution context
  
/*  A function execution context is created whenever a function is called, representing the function’s local scope. */


